
package com.binktec.sprint.modal.pojo.shop;


public class ShopCost {

    private ShopBindingCost shopBindingCost;
    private ShopColorCost shopColorCost;
    private ShopGrayscaleCost shopGrayscaleCost;

    public ShopBindingCost getShopBindingCost() {
        return shopBindingCost;
    }

    public void setShopBindingCost(ShopBindingCost shopBindingCost) {
        this.shopBindingCost = shopBindingCost;
    }

    public ShopColorCost getShopColorCost() {
        return shopColorCost;
    }

    public void setShopColorCost(ShopColorCost shopColorCost) {
        this.shopColorCost = shopColorCost;
    }

    public ShopGrayscaleCost getShopGrayscaleCost() {
        return shopGrayscaleCost;
    }

    public void setShopGrayscaleCost(ShopGrayscaleCost shopGrayscaleCost) {
        this.shopGrayscaleCost = shopGrayscaleCost;
    }

}
